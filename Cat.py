class Cat1:
    def __init__(self, name, gender, breed, age, status):
        self.name = name
        self.gender = gender
        self.breed = breed
        self.age = age
        self.status = status
    def getName(self):
        return self.name
    def getGender(self):
        return self.gender
    def getBreed(self):
        return self.breed
    def getAge(self):
        return self.age
    def getStatus(self):
        return self.status